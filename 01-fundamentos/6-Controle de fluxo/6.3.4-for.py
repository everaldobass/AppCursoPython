# loops em Python
print("\n Lista de Nomes em python usando um FOR: ")

nomes = ['Everaldo', 'Edson', 'Eduardo', 'Elias', 'Eriberto', 'Henrique']
for nome in nomes:
    print("\n Olá my brother: " + nome)


print("\n Lista de Frutas em python usando um FOR: ")

frutas = ['Banana', 'Laranja', 'Maça', 'Pera', 'Uva']
for fruta in frutas:
    print("\n Essa fruta é uma: " + fruta)